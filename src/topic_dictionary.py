def normalize_venue_name(venue: str) -> str:
    normalized = str(venue).strip().upper()
    if normalized in {"NIPS", "NEURIPS"}:
        return "NEURIPS"
    return normalized


def topic_dict_for_venue(venue: str) -> dict[str, dict[str, list[str] | str]]:
    normalized = normalize_venue_name(venue)
    if normalized in NEURIPS_VENUES:
        return NEURIPS_TOPIC_DICT
    return DB_TOPIC_DICT
